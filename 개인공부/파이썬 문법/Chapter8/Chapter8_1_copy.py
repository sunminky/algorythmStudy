import copy

a = [1, 2, 3]
b = [4, 5, a]
x = [a, b, 100]
y1 = copy.copy(x)    #얕은 복사
y2 = copy.deepcopy(x)   #깊은 복사

a[0] = -1
b[0] = -4

print(y1)   #[[-1, 2, 3], [-4, 5, [-1, 2, 3]], 100]
print(y2)   #[[1, 2, 3], [4, 5, [1, 2, 3]], 100]

#슬라이싱과 dict.copy()는 얕은복사, 슬라이싱은 리스트를 새로만들고 참조를 복사
L = [1, 2, 3, 4, 5]
L2 = L[1:3]
L[1] = 'a'

print(L)    #[1, 'a', 3, 4, 5]
print(L2)   #[2, 3]
